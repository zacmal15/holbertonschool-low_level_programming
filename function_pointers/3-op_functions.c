#include "3-calc.h"

/**
 * op_add - adds two ints
 * @a: first int
 * @b: second int
 *
 * Return: result of add
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two ints
 * @a: first int
 * @b: second int
 *
 * Return: result of sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two ints
 * @a: first int
 * @b: second int
 *
 * Return: result of mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two ints
 * @a: first int
 * @b: second it
 *
 * Return: result of div
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of div
 * @a: first int
 * @b: second int
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
